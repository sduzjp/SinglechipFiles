void lcd_wdat(uchar dat)   //LCD写数据子程序
{                          
	lcd_busy();
	LCD_RS=1;
	LCD_RW=0;
	LCD_data=dat;
	LCD_EN=1;
	_nop_();
	_nop_();
	LCD_EN=0;
}

void SelectScreen(uchar screen)	   //选屏子程序，screen=0,1,2
{
	switch(screen)
	{
		case 0: CS1=0;	//全屏显示
				_nop_();
				_nop_();
				_nop_();
				CS2=0;
				_nop_();
				_nop_();
				_nop_();
				break;
		case 1: CS1=0;	//左屏显示
				_nop_();
				_nop_();
				_nop_();
				CS2=1;
				_nop_();
				_nop_();
				_nop_();
				break;
		case 2: CS1=1;	//右屏显示
				_nop_();
				_nop_();
				_nop_();
				CS2=0;
				_nop_();
				_nop_();
				_nop_();
				break;
	}	
}

void ClearScreen(uchar screen)	//清屏子程序，screen=0,1,2
{
	uchar i,j;
	SelectScreen(screen);
	for(i=0;i<8;i++)
	{
		SetPage(i);
		SetColumn(0);
		for(j=0;j<64;j++)
		{
			lcd_wdat(0x00);
		}
	}
}

void lcd_init()		 //lcd12864初始化子程序
{
	lcd_busy();	 //读忙信号，不忙则向下执行
	SelectScreen(0);
	SetOnOff(0);   //关屏
	SelectScreen(0);
	SetOnOff(1);   //开屏
	SelectScreen(0);
	ClearScreen(0);	   //清屏
	SetStartLine(0);	//开始行为0	
}

